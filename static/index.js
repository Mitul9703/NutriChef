function handleCheckboxChange(checkboxId) {
    const checkboxElement = document.getElementById(checkboxId);
    const checkboxValue = checkboxElement.value;
  
    if (checkboxElement.checked && !selectedCheckboxes.includes(checkboxValue)) {
      selectedCheckboxes.push(checkboxValue);
    } else if (!checkboxElement.checked && selectedCheckboxes.includes(checkboxValue)) {
      selectedCheckboxes = selectedCheckboxes.filter(value => value !== checkboxValue);
    }
  }

